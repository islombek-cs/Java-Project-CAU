public class ProFourth {
    private int count;

    public ProFourth(int count) {
        this.count = count;
    }

    public void increment() {
        this.count++;
    }

    public void decrement() {
        this.count--;
    }

    public int getCount() {
        return this.count;
    }

    public void setCount(int count){
        this.count = count;
    }

}

